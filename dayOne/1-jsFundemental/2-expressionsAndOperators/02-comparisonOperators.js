// will use this for conditions

//== equal (doesn't check for data type)
console.log("3" == 3);
// === strict equal (does check for data type)
console.log("3" === 3);

//!= not equal 
console.log("3" != 3);
//!== strict not equal
console.log("3" !== 3);

// > greater than
console.log(3>1);

// < less Than
console.log(3<1);

//greater than or equal to 
console.log(3 >= 1);

//less than or equal
console.log( 5 <= 1);

// && and
console.log(1 > 0 && 2 > 1);

// || or
console.log( 1 > 0 || 2 > 7);